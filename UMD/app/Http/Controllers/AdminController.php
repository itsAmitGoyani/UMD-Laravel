    public function createManager(Request $request)
    {
        $manager = new Manager();
        $manager->name = $request->name;
        $manager->email = $request->email;
        $manager->password = $request->password;
        $manager->ngo_id = $request->ngo_id;
        $manager->save();
        return redirect('/admin-createmanager');
    }
